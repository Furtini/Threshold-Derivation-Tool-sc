    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        JFileChooser arquiv = new JFileChooser();
        arquiv.setMultiSelectionEnabled(true);
        if (jRadioButton4.isSelected()) {// filtro para arquivos de percentis
            arquiv.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return (f.getName().toLowerCase().endsWith(".csv")
                            && f.getName().toLowerCase().startsWith("metr")
                            && f.getName().contains("DATA")) || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files (.csv)";
                }
            });
        } else if (jRadioButton5.isSelected()) {// filtro para arquivos de copliance rate
            arquiv.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return f.getName().toLowerCase().endsWith(".csv")
                            && f.getName().toLowerCase().startsWith("crat")
                            && f.getName().contains("DATC") || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files (.csv)";
                }
            });
        } else if (jRadioButton6.isSelected()) {// filtro para arquivos de compliance rate of penality
            arquiv.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return (f.getName().toLowerCase().endsWith(".csv"))
                            && f.getName().toLowerCase().startsWith("weig")
                            && f.getName().contains("DATW") || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files (.csv)";
                }
            });
        }
        int retorno = arquiv.showOpenDialog(null);
        if (retorno == JFileChooser.APPROVE_OPTION) {
            File[] files = arquiv.getSelectedFiles();
            filesNamesG = new ArrayList<String>();
            for (int i = 0; i < files.length; i++) {
                filesNamesG.add(files[i].getName());
            }
            pathOrigemG = files[0].getParent();
            jLabel3.setText("Select the csv Files in: " + pathOrigem);
            jList2.setListData(filesNamesG.toArray());
        } else {
            JOptionPane.showMessageDialog(null, "Invalid File");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

