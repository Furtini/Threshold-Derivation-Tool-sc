    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser arquivo = new JFileChooser();
        arquivo.setMultiSelectionEnabled(true);
        if (jRadioButton1.isSelected()) {// filtro para arquivos de percentis
            arquivo.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return (f.getName().toLowerCase().endsWith(".csv")
                            && f.getName().toLowerCase().startsWith("metr")
                            && !f.getName().contains("DATA")
                            && !f.getName().contains("HEAD")) || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files of percentiles (.csv)";
                }
            });
        } else if (jRadioButton2.isSelected()) {// filtro para arquivos de copliance rate
            arquivo.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return (f.getName().toLowerCase().endsWith(".csv")
                            && f.getName().toLowerCase().startsWith("crat")
                            && !f.getName().contains("DATC")
                            && !f.getName().contains("HEAD")) || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files (.csv)";
                }
            });
        } else if (jRadioButton3.isSelected()) {// filtro para arquivos de compliance rate of penality
            arquivo.setFileFilter(new javax.swing.filechooser.FileFilter() {
                //Filtro, converte as letras em minúsculas antes de comparar
                public boolean accept(File f) {
                    return (f.getName().toLowerCase().endsWith(".csv")
                            && f.getName().toLowerCase().startsWith("weig")
                            && !f.getName().contains("DATW")
                            && !f.getName().contains("HEAD")) || f.isDirectory();
                }
                //Texto que será exibido para o usuário

                public String getDescription() {
                    return "Files (.csv)";
                }
            });
        }
        int retorno = arquivo.showOpenDialog(null);
        if (retorno == JFileChooser.APPROVE_OPTION) {
            File[] files = arquivo.getSelectedFiles();
            filesNames = new ArrayList<String>();
            for (int i = 0; i < files.length; i++) {
                filesNames.add(files[i].getName());
            }
            pathOrigem = files[0].getParent();
            jLabel1.setText("Select the csv Files in: " + pathOrigem);
            jList1.setListData(filesNames.toArray());
        } else {
            JOptionPane.showMessageDialog(null, "Invalid File");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
