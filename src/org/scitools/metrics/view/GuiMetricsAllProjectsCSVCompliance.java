    class Task extends SwingWorker<Void, Void> {
        /*
         * Main task. Executed in background thread.
         */

        @Override
        public Void doInBackground() {
            jButton7.setEnabled(false);

            CGAllFilesCompliance gafc = new CGAllFilesCompliance();

            //Initialize progress property.
            setProgress(0);

            int qtd = filesNames.size(), cont = 0;

            jLabel3.setText("Number of identify metrics: " + qtd);

            for (String x : filesNames) {
                jLabel2.setText("File in course: " + x);
                gafc.geraCompilances(pathOrigem, jTextField1.getText(), x, jSlider2.getValue(), jSlider1.getValue(), Integer.parseInt(jFormattedTextField2.getText()), Integer.parseInt(jFormattedTextField1.getText()));
                setProgress(Math.round((float) 100 / qtd) * ++cont);
            }


            return null;
        }

        /*
         * Executed in event dispatching thread
         */
        @Override
        public void done() {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Finish!");
            setProgress(100);
            //startButton.setEnabled(true);
            jLabel2.setText("Finish.");
            jButton7.setEnabled(true);

            //taskOutput.append("Done!\n");
        }
    }

